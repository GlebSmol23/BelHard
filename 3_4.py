#Пользователь вводит 3 числа, сказать сколько из них положительных и сколько отрицательных

num1 = float(input('Введите 1 число: '))
num2 = float(input('Введите 2 число: '))
num3 = float(input('Введите 3 число: '))

if num1 < 0:
    x1 = True
else:
    x1 = False

if num2 < 0:
    x2 = True
else:
    x2 = False

if num3 < 0:
    x3 = True
else:
    x3 = False

negative = x1 + x2 + x3
positive = 3 - negative
print('Отрицательных чисел:', negative)
print('Положительных чисел:', positive)
